class Coordinate:
    """
    A class to represent a coordinate in 3D space.
    """

    def __init__(self, x: float, y: float, z: float):
        """
        Initialize the Coordinate object with x, y, and z coordinates.

        :param x: The x coordinate.
        :param y: The y coordinate.
        :param z: The z coordinate.
        """
        self.x = x
        self.y = y
        self.z = z
